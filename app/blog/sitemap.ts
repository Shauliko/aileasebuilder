export default function sitemap() {
  const base = process.env.NEXT_PUBLIC_SITE_URL;

  return [
    {
      url: `${base}/`,
      lastModified: new Date().toISOString(),
    },
    {
      url: `${base}/pricing`,
      lastModified: new Date().toISOString(),
    },
    {
      url: `${base}/blog`,
      lastModified: new Date().toISOString(),
    },
  ];
}
